import java.util.*;
import java.io.*;
 
 
public class Print {
 
    private final int num;
    private final String name;
    public Print(int num, String name) {
         
        this.num = num;
        this.name = name;
    }
     
     
    public int getNum() {
        return num;
    }
    public String getName() {
        return name;
    }
     
     
     
}
